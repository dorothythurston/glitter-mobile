import UIKit

extension UIColor {
    static func barelyPinkColor() -> UIColor {
        return UIColor(red: 255/255, green: 246/255, blue: 254/255, alpha: 1)
    }
    
    static func bubblegumPinkColor() -> UIColor {
        return UIColor(red: 255/255, green: 182/255, blue: 218/255, alpha: 1)
    }
    
    static func flamingoPinkColor() -> UIColor {
        return UIColor(red: 255/255, green: 131/255, blue: 193/255, alpha: 1)
    }
    
    static func magentaPinkColor() -> UIColor {
        return UIColor(red: 255/255, green: 105/255, blue: 180/255, alpha: 1)
    }
}