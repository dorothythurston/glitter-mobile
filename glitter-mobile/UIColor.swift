import UIKit

extension UIColor {
    static func barelyPurpleColor() -> UIColor {
        return UIColor(red: 240/255, green: 230/255, blue: 249/255, alpha: 1)
    }
    
    static func greyPurpleColor() -> UIColor {
        return UIColor(red: 180/255, green: 134/255, blue: 228/255, alpha: 1)
    }
    
    static func princessPurpleColor() -> UIColor {
        return UIColor(red: 150/255, green: 85/255, blue: 217/255, alpha: 1)
    }
    
    static func brightPurpleColor() -> UIColor {
        return UIColor(red: 105/255, green: 13/255, blue: 201/255, alpha: 1)
    }
}